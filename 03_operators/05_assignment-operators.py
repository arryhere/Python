'''
• Assignment Operators
• Assignment operators are used to assign values to the variables
'''

x = 5
y = 10

x += 2    # 7
x -= 4    # 3
x *= 10   # 30
x /= 5    # 6
x %= 4    # 2
x **= 5   # 32
x //= 6   # 5

print(x)  # 5.0

y &= 2    # 2, 1010 & 0010 = 0010
y |= 13   # 15, 0010 | 1101 = 1111
y ^= 4    # 11, 1111 ^ 0100 = 1011
y >>= 2   # 2, 1011 >> 2 = 0010
y <<= 4   # 32, 0010 << 4 = 100000

print(y)  # 32


