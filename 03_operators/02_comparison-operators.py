'''
• Comparison Operators
• Comparison or Relational operators compares the values. It either returns True or False according to the condition
'''
x = 5
y = 10

greaterThan = x > y
lesserThan = x < y
equalTo = x == y
notEqualTo = x != y
greaterThanOrEqualTo = x >= y
lesserThanOrEqualTo = x <= y

print(greaterThan)              # False
print(lesserThan)               # True
print(equalTo)                  # False
print(notEqualTo)               # True
print(greaterThanOrEqualTo)     # False
print(lesserThanOrEqualTo)      # True


