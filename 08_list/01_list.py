'''
• Python Lists
• Lists are ordered collection of data items
• They store multiple items in a single variable
• For context its's just like Arrays from other programming languages
• Lists are mutable, we can change them after declaration

• List indexing:
  • ['p', 'y', 't', 'h', 'o', 'n']
  •   0    1    2    3    4    5
  •  -6   -5   -4   -3   -2   -1
'''


list = [3, 5, 7]
print(list, type(list))  # [3, 5, 7] <class 'list'>
print(list[0], list[1], list[2])  # 3 5 7


box = [2, 3.14, True, 'string', None]
print(box)  # [2, 3.14, True, 'string', None]
